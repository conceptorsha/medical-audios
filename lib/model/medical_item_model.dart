class MedicalItem {
  final String image;
  final String title;
  final String description;
  final String time;
  final double rating;
  final int ratingCount;

  MedicalItem({
    required this.image,
    required this.title,
    required this.description,
    required this.time,
    required this.rating,
    required this.ratingCount,
  });
}